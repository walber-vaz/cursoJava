package br.com.wssoftwares.fundamentos;

public class Atribuicao {
    public static void main(String[] args) {
        // Operador de atribuição
        // = += -= *= /= %=
        int a = 10;
        a += 5; // a = a + 5
        System.out.println(a);

        a -= 5; // a = a - 5
        System.out.println(a);

        a *= 5; // a = a * 5
        System.out.println(a);

        a /= 5; // a = a / 5
        System.out.println(a);

        a %= 5; // a = a % 5
        System.out.println(a);
    }
}
